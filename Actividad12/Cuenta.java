package Actividad12;

public class Cuenta {

        String dni;
        String nombre;
        String numeroCuenta;
        double saldo;

        public Cuenta(String dni, String nombre, String numeroCuenta, double saldo) {
            this.dni = dni;
            this.nombre = nombre;
            this.numeroCuenta = numeroCuenta;
            this.saldo = saldo;
        }
    }

